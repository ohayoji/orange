jQuery(function ($) {
	/*--ナビゲーション-------------*/
	var navContents = $.navContents();
	$.setNav(navContents);
	/*---------------------------*/
	
	/*--FAQリスト表示---------------------------*/
	var categoryTemp = $("#temp_faq_category");
	$.each(faq,function(index,val){
		//テンプレクローンして表示
		var div = categoryTemp.clone().removeAttr("id").show().appendTo("#faq_area");
		//カテゴリタイトル表示
		div.find("h2").text(val.category);
		
		//リスト表示
		var dl = div.find("dl");
		var dtTemp = div.find("dt.temp_faq_dt");
		var ddTemp = div.find("dd.temp_faq_dd");
		$.each(val.faq,function(i,v){
			//テンプレクローンして表示
			var dt = dtTemp.clone().removeClass("temp_faq_dt").appendTo(dl).text(v.q);
			var dd = ddTemp.clone().removeClass("temp_faq_dd").appendTo(dl).html(v.a);
			dt.on("click",function(){
				//クリックでスライド表示切り替え
				dd.slideToggle(100);
			});
		});
		//テンプレ削除
		dtTemp.remove();
		ddTemp.remove();
	});
	//テンプレ削除
	categoryTemp.remove();
	/*-----------------------------------------*/
	
	/*--フッター設置--------------------------*/
	//リンク配列
	var navLinks = $.getOrangePassCategories(1);//@url_converter.js
	
	//フッター作成
	$.setMyFooter({footerNav:navLinks});
	/*---------------------------------------*/
});